import java.util.ArrayList;//Import package for using ArrayList
public class ArrayLists {
    public static void main(String[] args) {
        //Array List's are dynamic , since it is resizable unlike Arrays
        ArrayList list=new ArrayList();//Declaration of array list
        list.add(45);//Adding elements in ArrayList using add method
        list.add(23);
        System.out.println(list);//Printing ArrayList
    }
}
